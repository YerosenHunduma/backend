import { Schema, model } from "mongoose";

const ContactUsSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const ContactUs = model("ContactUs", ContactUsSchema);

export default ContactUs;
